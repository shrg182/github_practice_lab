#!/usr/bin/env python3
"""
Practice 1: Try expressions like you would in the Python interpreter.
"""


def main() -> None:
    print("Expression practice")
    print(f"3 + 4 = {3 + 4}")
    print(f"12 / 5 = {12 / 5}")
    print(f"12 // 5 = {12 // 5}")
    print(f"12 % 5 = {12 % 5}")
    print(f"2 ** 5 = {2 ** 5}")


if __name__ == "__main__":
    main()
